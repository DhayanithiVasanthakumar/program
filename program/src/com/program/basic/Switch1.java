package com.program.basic;

public class Switch1 {
	
	
	    public static void main(){
	        int n=10;
	        
	        switch(n%2){
	            case 0:
	                System.out.println("even");
	                break;
	            case 1:
	                System.out.println("odd");
	                break;

	        }
	    }
	    public static void main(String[]args) {
	    	Switch1 check=new Switch1();
	    	check.main();
	    }
	}

